//Bucle for que simula un castigo en el que Bart Simpson debe escribir una frase 10 veces en la pizarra.
#include <iostream> //Incluimos librería iostream que permite la entrada por teclado y la salida por pantalla.
using namespace std; //Sentencia obligatoria.

//Inicio del programa.
int main () {
	for (int castigo=1; castigo <=10; castigo++) { //Declaración e inicialización de variable e inicio del bucle.
		cout << castigo << ". No organizaré mis propios simulacros de incendio" << endl; //Sacamos las frase por pantalla.
	}
}
